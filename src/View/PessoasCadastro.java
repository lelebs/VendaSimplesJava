    private void validarMask()
    {
        try 
        {
            MaskFormatter maskCpf = new MaskFormatter("###.###.###-##");
            MaskFormatter maskCnpj = new MaskFormatter("##.###.###/####-##");
            
            if(comboTipoPessoa.getSelectedIndex() == 0)
            {
                lblCpfCnpj.setText("CPF");
                txtCpfCnpj.setFormatterFactory(new DefaultFormatterFactory(maskCpf));
            }
            
            else if(comboTipoPessoa.getSelectedIndex() == 1)
            {
                lblCpfCnpj.setText("CNPJ");
                txtCpfCnpj.setFormatterFactory(new DefaultFormatterFactory(maskCnpj));
            }
        } 
        catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
