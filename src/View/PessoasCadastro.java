    private void bttnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnSalvarActionPerformed
        
        String razaoSocialStr = txtRazaoSocial.getText();
        String cpfCnpjStr = txtCpfCnpj.getText();
        
        try
        {
            String cpfCnpjSemMaskStr = txtCpfCnpj.getText().replace(".", "");
            cpfCnpjSemMaskStr = cpfCnpjSemMaskStr.replace("/", "");
            cpfCnpjSemMaskStr = cpfCnpjSemMaskStr.replace("-", "");
            cpfCnpjSemMaskStr = cpfCnpjSemMaskStr.replace(" ", "");
            
            Helper.ConverterCamposHelper.validarCampoVazio(lblCpfCnpj.getText(), cpfCnpjSemMaskStr);
            Helper.ConverterCamposHelper.validarCampoVazio("Razáo Social", txtRazaoSocial.getText());
            Helper.ConverterCamposHelper.validarCampoVazio("Data de Nascimento", dtNascimento.getDateFormatString());
            
            Helper.ConverterCamposHelper.validarMaiorQue("Razão Social", razaoSocialStr, 50);
            Helper.ConverterCamposHelper.validarMaiorQue(lblCpfCnpj.getText(), cpfCnpjStr, 20);

            Pessoa pessoa = new Pessoa();
            pessoa.setRazaoSocial(txtRazaoSocial.getText());
            pessoa.setTipoPessoa(comboTipoPessoa.getSelectedItem().toString());
            pessoa.setCpfCnpj(txtCpfCnpj.getText());
            pessoa.setDataNascimento(dtNascimento.getDate());  
            if(checkAtivo.isSelected())
            {
                pessoa.setAtivo("Sim");
            }
            else
            {
                pessoa.setAtivo("Não");
            }
            
            if(acaoFrame == INSERIR && Pessoa.validarSeCpfCnpjExiste(pessoa.getCpfCnpj()) == false)
            {
                pessoa = Pessoa.inserirPessoa(pessoa);
                PessoasPanel.adicionarNaTabela(pessoa);
                this.dispose();
            }

            else if(acaoFrame == INSERIR && Pessoa.validarSeCpfCnpjExiste(pessoa.getCpfCnpj()) == true)
            {
                    Helper.MostrarMensagem.mostrarErroMensagem("Registro já cadastrado!");
            }

            else
            {
                pessoa.setIdPessoa(idPessoa);
                PessoasPanel.limparTabela();
                PessoasPanel.adicionarNaTabela(Pessoa.alterarPessoa(pessoa));
                this.dispose();
            }
        }
        
        catch(ConverterCamposDoubleException | LimiteCampoException | CampoVazioException | BancoException ex)
        {
            Helper.MostrarMensagem.mostrarErroMensagem(ex.getMessage());
        }    
    }//GEN-LAST:event_bttnSalvarActionPerformed

