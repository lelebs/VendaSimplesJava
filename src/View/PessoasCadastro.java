    private void receberPessoa(Pessoa pessoa)
    {
        if(pessoa != null)
        {
            idPessoa = pessoa.getIdPessoa();
            this.txtId.setText(String.valueOf(pessoa.getIdPessoa()));
            this.txtRazaoSocial.setText(pessoa.getRazaoSocial());
            
            switch(pessoa.getTipoPessoa())
            {
                case "Física": comboTipoPessoa.setSelectedIndex(0); break;
                case "Jirídica": comboTipoPessoa.setSelectedIndex(1); break;
            }
                    
            String cpfCnpjSemMaskStr = pessoa.getCpfCnpj().replace(".", "");
            cpfCnpjSemMaskStr = cpfCnpjSemMaskStr.replace("/", "");
            cpfCnpjSemMaskStr = cpfCnpjSemMaskStr.replace("-", "");
            cpfCnpjSemMaskStr = cpfCnpjSemMaskStr.replace(" ", "");
            
            this.txtCpfCnpj.setValue(pessoa.getCpfCnpj());
            
            this.dtNascimento.setDate(pessoa.getDataNascimento());
            
            if("Não".equals(pessoa.isAtivo()))
            {
                this.checkAtivo.setSelected(false);
            }
        }
    }
    
